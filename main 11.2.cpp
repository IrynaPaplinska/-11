// –§—É–Ω–∫—Ü—ñ—è, —è–∫–∞ –æ–±—á–∏—Å–ª—é—î c, a —Ç–∞ y
// –ó–∞—É–≤–∞–∂–µ–Ω–Ω—è: –≤ C++ —Ñ—É–Ω–∫—Ü—ñ—è –ø–æ–≤–µ—Ä—Ç–∞—î –ª–∏—à–µ –æ–¥–Ω–µ –∑–Ω–∞—á–µ–Ω–Ω—è.
// –©–æ–± –ø–æ–≤–µ—Ä–Ω—É—Ç–∏ –∫—ñ–ª—å–∫–∞ (c, a, y), –º–∏ –±—É–¥–µ–º–æ –ø–µ—Ä–µ–¥–∞–≤–∞—Ç–∏ —ó—Ö –∑–∞ –ø–æ—Å–∏–ª–∞–Ω–Ω—è–º (&)
// –∞–±–æ –≤–∏–∫–æ—Ä–∏—Å—Ç–æ–≤—É–≤–∞—Ç–∏ —Å—Ç—Ä—É–∫—Ç—É—Ä—É/–∫–æ—Ä—Ç–µ–∂. –¢—É—Ç –≤–∏–∫–æ—Ä–∏—Å—Ç–∞–Ω–æ –ø–æ—Å–∏–ª–∞–Ω–Ω—è.
void calculate_trig_function(double k, double x, double &c, double &a, double &y) {
    
    // --- 1. –û–±—á–∏—Å–ª–µ–Ω–Ω—è –ø—Ä–æ–º—ñ–∂–Ω–æ—ó –∑–º—ñ–Ω–Ω–æ—ó c ---
    // c = arctan(|x|)
    // std::fabs(x) - –æ–±—á–∏—Å–ª—é—î –º–æ–¥—É–ª—å —á–∏—Å–ª–∞ –∑ –ø–ª–∞–≤–∞—é—á–æ—é –∫–æ–º–æ—é
    // std::atan(...) - –æ–±—á–∏—Å–ª—é—î –∞—Ä–∫—Ç–∞–Ω–≥–µ–Ω—Å (—Ä–µ–∑—É–ª—å—Ç–∞—Ç –≤ —Ä–∞–¥—ñ–∞–Ω–∞—Ö)
    c = std::atan(std::fabs(x));

    // --- 2. –û–±—á–∏—Å–ª–µ–Ω–Ω—è –ø—Ä–æ–º—ñ–∂–Ω–æ—ó –∑–º—ñ–Ω–Ω–æ—ó a ---
    // a = c + k^2
    a = c + k * k; // k*k —à–≤–∏–¥—à–µ —ñ –ø—Ä–æ—Å—Ç—ñ—à–µ, –Ω—ñ–∂ std::pow(k, 2)

    // --- 3. –û–±—á–∏—Å–ª–µ–Ω–Ω—è –∫—ñ–Ω—Ü–µ–≤–æ—ó —Ñ—É–Ω–∫—Ü—ñ—ó y ---
    // y = sin^3(a) + cos^2(x)

    // –û–±—á–∏—Å–ª–µ–Ω–Ω—è sin^3(a) = (sin(a))^3
    double sin_a = std::sin(a);
    double sin_a_cubed = sin_a * sin_a * sin_a; // –ê–±–æ std::pow(sin_a, 3.0)

    // –û–±—á–∏—Å–ª–µ–Ω–Ω—è cos^2(x) = (cos(x))^2
    double cos_x = std::cos(x);
    double cos_x_squared = cos_x * cos_x; // –ê–±–æ std::pow(cos_x, 2.0)

    y = sin_a_cubed + cos_x_squared;
}

