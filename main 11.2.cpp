    // –í—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–Ω—è –ª–æ–∫–∞–ª—ñ –¥–ª—è –∫–æ—Ä–µ–∫—Ç–Ω–æ—ó —Ä–æ–±–æ—Ç–∏ –∑ —á–∏—Å–ª–∞–º–∏ (–Ω–∞–ø—Ä–∏–∫–ª–∞–¥, –∫—Ä–∞–ø–∫–∞ –∑–∞–º—ñ—Å—Ç—å –∫–æ–º–∏)
    // —Ç–∞ –≤—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–Ω—è —Ç–æ—á–Ω–æ—Å—Ç—ñ –≤–∏–≤–æ–¥—É
    std::cout << "üöÄ –ü—Ä–æ–≥—Ä–∞–º–∞ –¥–ª—è –æ–±—á–∏—Å–ª–µ–Ω–Ω—è y = sin^3(a) + cos^2(x) –Ω–∞ C++" << std::endl;
    std::cout << std::fixed << std::setprecision(6); // –í—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–Ω—è 6 –∑–Ω–∞–∫—ñ–≤ –ø—ñ—Å–ª—è –∫–æ–º–∏

    double k_input, x_input;
    double c_result, a_result, y_result; // –ó–º—ñ–Ω–Ω—ñ –¥–ª—è –∑–±–µ—Ä–µ–∂–µ–Ω–Ω—è —Ç—Ä—å–æ—Ö —Ä–µ–∑—É–ª—å—Ç–∞—Ç—ñ–≤

    // –í–≤–µ–¥–µ–Ω–Ω—è –¥–∞–Ω–∏—Ö
    std::cout << "–í–≤–µ–¥—ñ—Ç—å –∑–Ω–∞—á–µ–Ω–Ω—è k: ";
    if (!(std::cin >> k_input)) {
        std::cerr << "‚ùå –ü–æ–º–∏–ª–∫–∞: –í–≤–µ–¥–µ–Ω–æ –Ω–µ–∫–æ—Ä–µ–∫—Ç–Ω–µ —á–∏—Å–ª–æ–≤–µ –∑–Ω–∞—á–µ–Ω–Ω—è –¥–ª—è k." << std::endl;
        return 1;
    }

    std::cout << "–í–≤–µ–¥—ñ—Ç—å –∑–Ω–∞—á–µ–Ω–Ω—è x: ";
    if (!(std::cin >> x_input)) {
        std::cerr << "‚ùå –ü–æ–º–∏–ª–∫–∞: –í–≤–µ–¥–µ–Ω–æ –Ω–µ–∫–æ—Ä–µ–∫—Ç–Ω–µ —á–∏—Å–ª–æ–≤–µ –∑–Ω–∞—á–µ–Ω–Ω—è –¥–ª—è x." << std::endl;
        return 1;
    }

    // –í–∏–∫–ª–∏–∫ —Ñ—É–Ω–∫—Ü—ñ—ó: —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∏ –±—É–¥—É—Ç—å –∑–∞–ø–∏—Å–∞–Ω—ñ –≤ c_result, a_result, y_result
    calculate_trig_function(k_input, x_input, c_result, a_result, y_result);

