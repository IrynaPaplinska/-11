    std::cout << "1. –ü—Ä–æ–º—ñ–∂–Ω–∏–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç c (–≤ —Ä–∞–¥—ñ–∞–Ω–∞—Ö):" << std::endl;
